class Solution{
  public:
    int findExtra(int a[], int b[], int n) {
        int low=0,high=n-1;
        int mid=(low+high)/2;
        while(low<high){
            if(b[mid]>a[mid])
               high=mid; 
            else
                low=mid+1;
            mid=(low+high)/2;
        }
        return mid;
    }
};

